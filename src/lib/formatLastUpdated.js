/** Shared "Last updated" timestamp for dashboard, CD balance, and related surfaces. */
export function formatLastUpdated(iso) {
  try {
    return new Date(iso).toLocaleString('en-IN', {
      day: '2-digit',
      month: 'short',
      year: 'numeric',
      hour: '2-digit',
      minute: '2-digit',
    })
  } catch {
    return iso
  }
}
